package JavaBasic;

public class A {
	
	public A() {
		System.out.println("Parent Class Constructor");
	}
	
	public A(int i) {
		
		System.out.println("Parent Class Constructor"+i);
	}
	
	public A(int i,int j) {
		
		System.out.println("Parent Class Constructor"+i);
		System.out.println("Parent Class Constructor"+j);
	}

}
